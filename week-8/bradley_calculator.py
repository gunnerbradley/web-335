def add(argument1, argument2):
    return argument1 + argument2

def subtract(argument1, argument2):
    return argument1 - argument2

def divide(argument1, argument2):
    return argument1 / argument2

print(add(1, 2))
print(subtract(4, 1))
print(divide(8, 2))